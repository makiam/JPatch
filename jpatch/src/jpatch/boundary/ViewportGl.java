	private void drawString(String string, int x, int y) {
		gl.glRasterPos2i(x, y);							// set raster position
		char[] c = string.toCharArray();
	    for (int i = 0, n = c.length; i < n; i++) {		// loop over characters
	    	if (c[i] < 256) {
	    		gl.glCallList(fontOffset + c[i]);		// call display list that draws a character
	    	}
	    }
	}
	
	private void spatialMode() {
		float w = (float) component.getWidth() / 2;
		float h = (float) component.getHeight() / 2;
		gl.glMatrixMode(GL.GL_PROJECTION);
		gl.glLoadIdentity();
		if (camera != null) {
			float a = 17.5f / (float) camera.focalLength.get(); 	// 35/focallength/2
			float b = a * h / w;
			gl.glDepthFunc(GL.GL_LEQUAL);
			gl.glFrustum(-a, a, -b, b, nearClip, farClip);
		} else {
			gl.glDepthFunc(GL.GL_LEQUAL);
			gl.glOrtho(-w, w, -h, h, -farClip, farClip);
		}
		gl.glMatrixMode(GL.GL_MODELVIEW);
		gl.glLoadIdentity();
		gl.glEnable(GL.GL_DEPTH_TEST);
	}
	
	private void rasterMode() {
		int w = drawable.getWidth();
		int h = drawable.getHeight();
		gl.glViewport(0, 0, w - 1, h - 1);
	    gl.glMatrixMode(GL.GL_PROJECTION);
	    gl.glLoadIdentity();
	    gl.glOrtho(0, w - 1, h - 1, 0, 1, -1);
	    gl.glMatrixMode(GL.GL_MODELVIEW);
	    gl.glLoadIdentity();
	    gl.glDisable(GL.GL_LIGHTING);
		gl.glDisable(GL.GL_DEPTH_TEST);
		gl.glShadeModel(GL.GL_FLAT);
	}
