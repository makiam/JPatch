	/** Offset to the GL Display-Lists holding the character bitmaps. */
	private int fontOffset;
	
	private int maxLights, maxTextureSize;
	
	public ViewportGl(int id, View view, Iterable<Model> models) {
		super(id, view, models);
		drawable = LIGHTWEIGHT ? new GLJPanel() : new GLCanvas();
		component = (Component) drawable;
		component.setBackground(COLORS.background.get());
		drawable.addGLEventListener(new GLEventListener() {

			public void init(GLAutoDrawable drawable) {
				gl = drawable.getGL();
				gl.glEnd();
				
				/* Generate display lists to render font characters. */
				gl.glPixelStorei(GL.GL_UNPACK_ALIGNMENT, 1);
				fontOffset = gl.glGenLists(256);
				for (int i = 0; i < 256; i++) {
			        gl.glNewList(i + fontOffset, GL.GL_COMPILE);
			            gl.glBitmap(16, 12, 0, 0, fontAdvance[i], 0, fontRaster[i], 0);
			        gl.glEndList();
			    }
				
				/* retrieve GL_MAX_LIGHTS and GL_MAX_TEXTURE_SIZE */
				int[] I = new int[1];
				gl.glGetIntegerv(GL.GL_MAX_LIGHTS, I, 0);
				maxLights = I[0];
				gl.glGetIntegerv(GL.GL_MAX_TEXTURE_SIZE, I, 0);
				maxTextureSize = I[0];
			}

			public void display(GLAutoDrawable drawable) {
				gl.glClearColor(COLORS.background.x, COLORS.background.y, COLORS.background.z, 0);	// set background color
				gl.glClearDepth(CLEAR_DEPTH);									// set initial depth-buffer value
				gl.glClear(GL.GL_COLOR_BUFFER_BIT | GL.GL_DEPTH_BUFFER_BIT);	// clear color and depth buffers
				draw();
			}

			public void reshape(GLAutoDrawable drawable, int x, int y, int width, int height) {
				computeMatrices();
				init(drawable);
				display(drawable);
			}

			public void displayChanged(GLAutoDrawable drawable, boolean modeChanged, boolean deviceChanged) { }
		});
	}
	
